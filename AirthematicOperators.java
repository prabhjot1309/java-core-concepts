public class AirthematicOperators {
    public static void main(String[] args) {
        
        int a = 10;
        int b = 5;

        // Addition
        int sum = a + b;
        System.out.println("Sum: " + sum); // Outputs 15

        // Subtraction
        int difference = a - b;
        System.out.println("Difference: " + difference); // Outputs 5

        // Multiplication
        int product = a * b;
        System.out.println("Product: " + product); // Outputs 50

        // Division
        int quotient = a / b;
        System.out.println("Quotient: " + quotient); // Outputs 2

        // Modulus
        int remainder = a % b;
        System.out.println("Remainder: " + remainder); // Outputs 0
    }
}
